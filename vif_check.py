# vif test 3 - using statmodels
from statsmodels.stats.outliers_influence import variance_inflation_factor
import numpy as np

a = [1, 1, 2, 3, 4]
b = [2, 2, 3, 2, 1]
c = [4, 6, 7, 8, 9]
d = [4, 3, 4, 5, 4]

ck = np.column_stack([a, b, c, d])

vif = [variance_inflation_factor(ck,i) for i in range (ck.shape[1])]
print(vif)