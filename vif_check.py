# vif test 1 - using pandas
import numpy as np
import pandas as pd
import seaborn as sns

iris = sns.load_dataset('iris')

iris_cor = iris.corr()
iris_vif = pd.DataFrame(np.linalg.inv(iris.corr().values), index = iris_cor.index, columns=iris_cor.columns)
iris_vif.round(2)

